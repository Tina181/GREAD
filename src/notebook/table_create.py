def generate_latex_row(method_name, values):
    row = f"{method_name} &\n"
    for value in values:
        mean, std = value.split(' ± ')
        color = "black"  # 所有颜色均设为黑色
        row += f"         $ \\textcolor{{{color}}}{{{mean} \\pm {std}}} $ &\n"
    row += "         \\\\"
    return row

# 生成 LaTeX 行
